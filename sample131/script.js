AOS.init({
    duration: 1000,
    once: true,
    offset: 100,
  });
  
//コード解説
// javascript
// AOS.init({
// 📦 AOSの初期化を開始する関数呼び出し この init() 関数を使うことで、AOSのアニメーション処理を設定できます。
// 中にはオプションをオブジェクト形式で渡します。

//   duration: 1000,
// ⏱️ アニメーションの再生時間（ミリ秒） ここでは、アニメーションが 1秒間（1000ms） かけて実行されます。
//  例：duration: 500 にすれば、0.5秒で完了するより軽快な動きになります。

//   once: true,
// 🔁 アニメーションの再実行可否 true だと、アニメーションは 一度だけ 実行されます（スクロールで再表示されても再発火しません）。
//  → LPなどで一度だけ印象づけたい場合に有効。

//   offset: 100,
// 📐 スクロール発火位置の調整（px単位） 画面の下から要素が 100px手前に達した時点 でアニメーションが始まります。 
// → 通常より早めに演出を見せたい時に使います。

// });
// 🔚 初期化完了 全ての設定が終わり、AOSが動作するようになります。
// この時点でHTMLの data-aos 属性が有効になります。
// 🔍 AOSには他にも便利な設定があります：
// delay：アニメーション開始までの待機時間
// easing：動きの種類（例：ease-in-out）
// mirror：逆スクロールでも発火させるかどうか